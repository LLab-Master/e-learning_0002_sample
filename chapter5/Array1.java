package chapter5;

public class Array1 {

	public static void main(String[] args) {
		int[] arry;
		arry = new int[4];

		arry[0] = 10;
		arry[1] = 20;
		arry[2] = 15;
		arry[3] = 5;

		System.out.println("1番目のデータは " + arry[0]);
		System.out.println("2番目のデータは " + arry[1]);
		System.out.println("3番目のデータは " + arry[2]);
		System.out.println("4番目のデータは " + arry[3]);

	}

}
