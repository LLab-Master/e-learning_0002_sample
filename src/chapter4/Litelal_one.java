package chapter4;

public class Litelal_one {
	public static void main(String[] args) {
		// リテラルの種類		
		System.out.println(1);   // 整数リテラル
		System.out.println(1f);  // 浮動小数点リテラル
		System.out.println('1'); // 文字リテラル
		System.out.println("1"); // 文字列リテラル
		
		
		boolean shipping = true; // 真偽リテラル
		System.out.println(shipping);
		boolean hungry = false; // 真偽リテラル
		System.out.println(hungry);
	}

}